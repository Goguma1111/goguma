public class Ex01_변수의_사용 {
    public static void main(String[] args) {
        /* 선언과 활당을 분리 */
        int a;
        a = 100;
        System.out.println(a);


        int b = a;          // 정수형 b를 선언하고 a에 기록된 내용을 복사
        System.out.println(b);

        // 실수형 변수
        double c = 3.14;
        System.out.println(c);

        

        // 논리형 변수
        boolean d = true;
        System.out.println(d);


    }
}
