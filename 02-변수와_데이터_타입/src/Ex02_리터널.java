public class Ex02_리터널 {
    public static void main(String[] args) {
        int num1 = 100;  // 정수형태의 리터널은 int로 인식된다.
        System.out.println(num1); 

        double num2 = 100;
        System.out.println(num2);


        // 실수의 리터럴은 첫 글자를 지정해서 구분해야 함 (구분하지 않을 경우 double로 인식)

        float num3 = 3.14f;
        System.out.println(num3);

        double num4 = 3.15D;
        System.out.println(num4);


        double num5 = 100D;
        System.out.println(num5);

        
    }
}
