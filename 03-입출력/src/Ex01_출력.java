public class Ex01_출력 {
    public static void main(String[] args) {
        // 줄바꿈 수행하기
        
        System.out.println("j");
        System.out.println("a");
        System.out.println("v");
        System.out.println("a");


        /** 아무런 내용도 출력하지 않고 줄 바꿈 실행 */
        System.out.println();


        /** 줄바꿈 안하기 */
        System.out.print("j");
        System.out.print("a");
        System.out.print("v");
        System.out.print("a");

    }
    

}    
