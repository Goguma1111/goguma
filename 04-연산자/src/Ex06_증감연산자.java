public class Ex06_증감연산자 {
    public static void main(String[] args) {
       // 단항 연산식 표현
        int k = 100;
        k += 1;
        System.out.println(k);

        // 같은 처리에 대한 증감연산식 표현
        k++;
        System.out.println(k);


        // 같은 처리에 대한 증감연산식 표현
        ++k;
        System.out.println(k);



        // 뺄셈의 단항 연산자
        int l = 100;
        l -= 1;
        System.out.println(l);


        // 같은 처리에 대한 증감연산자

        l--;
        System.out.println(l);


        --l;
        System.out.println(l);



    }
    
}